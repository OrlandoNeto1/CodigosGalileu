#tecnica para seleção de um intervalo de listas, tuplas e dicionarios.
tupla_nomes = ('alvaro',
                'leticia',
                'bruno',
                'henrique',
                'carlos')
print(tupla_nomes[2:5])
print(tupla_nomes[0:3])
print(tupla_nomes[2:5])
print(tupla_nomes[::2])
print(tupla_nomes[::-1])


#[1:3]= posição onde é selecionado

