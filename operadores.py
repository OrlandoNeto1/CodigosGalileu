X =int(input("digite um numero "))
Y=int(input("Digite o segundo numero "))

#criando uma operação de soma e armazenando na variavel soma

soma = X+Y
print(f"Sua soma de {X}+{Y} deu {soma}")

#CRIANDO SUBTRAÇÃO NA VARIAVEL SUB
sub= X - Y
print(f"Sua subtração de {X}-{Y} deu {sub}")

#CRIANDO UMA MULTIPLICAÇÃO NA VARIAVEL MULTI
multi= X * Y
print(f"Sua multiplicação de {X}x{Y} deu {multi}")

#CRIANDO UMA DIVISAO E ARMAZENANDO NA VARIAVEL DIV
div = X / Y
print(f"Sua divisão de {X}x{Y} deu {div}")

#CRIANDO UMA OPERAÇÃO DE RESTO NA VARIAVEL RESTO
resto = X % Y
print(f"O resto da divisão de {X}/{Y} deu {resto}")